#import "UIColor+ST.h"

@implementation UIColor (ST)

+ (UIColor*)getSTBlue
{
    return [UIColor colorWithRed:0.26 green:0.73 blue:0.89 alpha:1];
}

+ (UIColor*)getSTGray
{
    return [UIColor colorWithRed:0.77 green:0.78 blue:0.79 alpha:1];
}

@end
